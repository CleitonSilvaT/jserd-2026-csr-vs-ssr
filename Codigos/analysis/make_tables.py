def main():
    """Main function to generate all tables."""
    if len(sys.argv) < 2:
        print("Usage: python make_tables.py <input_csv_path> [output_dir]")
        sys.exit(1)
    
    input_csv = Path(sys.argv[1])
    output_dir = Path(sys.argv[2]) if len(sys.argv) > 2 else Path("tables")
    
    if not input_csv.exists():
        print(f"Error: Input file {input_csv} not found")
        sys.exit(1)
    
    output_dir.mkdir(parents=True, exist_ok=True)
    
    print(f"Loading data from {input_csv}")
    df = load_data(input_csv)
    print(f"Loaded {len(df)} rows")
    
    print(f"\nGenerating tables in {output_dir}")
    
    for metric in METRICS:
        # Normalizar nome da coluna (remover espaços extras)
        metric_col = metric
        if metric_col not in df.columns:
            # Tentar variações do nome
            possible_names = [metric_col, metric_col.replace(" ", ""), metric_col.replace(" (s)", ""), metric_col.replace(" (ms)", "")]
            found = False
            for name in possible_names:
                if name in df.columns:
                    metric_col = name
                    found = True
                    break
            if not found:
                print(f"Warning: Metric {metric} not found in data, skipping")
                continue
        
        print(f"\nProcessing {metric} ({METRIC_NAMES.get(metric, metric)})...")
        
        # Summary statistics
        generate_summary_table(df, metric_col, output_dir)
        
        # Kruskal-Wallis test
        generate_kruskal_wallis_results(df, metric_col, output_dir)
        
        # Dunn's post-hoc test
        generate_dunn_results(df, metric_col, output_dir)
        
        # Cliff's Delta
        generate_cliffs_delta_results(df, metric_col, output_dir)
    
    print(f"\nAll tables generated in {output_dir}")


if __name__ == "__main__":
    main()

